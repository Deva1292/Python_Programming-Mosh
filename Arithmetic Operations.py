10  # integer
10.123 # float

print(10 + 3)  # Addition
print(10 * 3)  # Multiplication
print(10 - 3)  # Subtraction
print(10 / 3)  # Division resulting in float
print(10 // 3)  # Division resulting in integer
print(10 % 3)  # modulus operator # returns the remainder of the division
print(10 ** 3)  # exponential operator

x = 10
x = x + 3
x += 3  #Augumented Assignment Operator # means the same thing as before
x -= 3
print(x)